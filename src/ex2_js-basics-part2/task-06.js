const INPUT_VALUE = 2;
const MAX_VALUE = 1000;

checkTypeNumber(INPUT_VALUE);// eslint-disable-line no-use-before-define

module.exports = checkTypeNumber;// eslint-disable-line no-use-before-define

function checkTypeNumber(value) {
  let amountDivide = 0;
  let returnedSting = '';

  if ((value > MAX_VALUE) || (value < 0)) {
    return '–î–∞–Ω–Ω—ã–µ –Ω–µ–≤–µ—Ä–Ω—ã';
  } if ((value === 1) || (value === 0)) {
    return '–ù–µ –ø—Ä–∏—á–∏—Å–ª—è–µ—Ç—Å—è –Ω–∏ –∫ –ø—Ä–æ—Å—Ç—ã–º, –Ω–∏ –∫ —Å–æ—Å—Ç–∞–≤–Ω—ã–º —á–∏—Å–ª–∞–º';
  }

  for (let i = 0; i < value + 1; i++) {
    if (value % i === 0) {
      amountDivide++;
    }
  }

  if (amountDivide === 2) {
    returnedSting = `–ß–∏—Å–ª–æ ${value} - –ø—Ä–æ—Å—Ç–æ–µ —á–∏—Å–ª–æ`;

    return returnedSting;
  } if (amountDivide > 2) {
    returnedSting = `–ß–∏—Å–ª–æ ${value} - —Å–æ—Å—Ç–∞–≤–Ω–æ–µ —á–∏—Å–ª–æ`;

    return returnedSting;
  }

  return '';
}
